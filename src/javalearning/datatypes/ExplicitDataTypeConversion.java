package javalearning.datatypes;

public class ExplicitDataTypeConversion {

	public static void main(String[] args) {
		
		double a = 50.50;
		System.out.println(" Double represntation: "+ a);
		
		float f = (float)a;
		System.out.println(" Float represntation: "+ f);
		
		long b = (long)a;
		System.out.println(" Long represntation: "+ b);
		
		int c = (int)b;
		System.out.println(" Int represntation: "+ c);

	}

}
